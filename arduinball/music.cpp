#include <Arduino.h>
#include "music.h"


/* Play a music as specified in the given 'music' structure. */
void playMusic(music m) {
  // Public domain code taken from http://arduino.cc/en/Tutorial/Tone
  for (int thisNote = 0; thisNote < m.length; thisNote++) {

    // to calculate the note duration, take one second
    // divided by the note type.
    //e.g. quarter note = 1000 / 4, eighth note = 1000/8, etc.
    int noteDuration = 1000 / m.notesDuration[thisNote];
    tone(m.speakerPin, m.notes[thisNote], noteDuration);

    // to distinguish the notes, set a minimum time between them.
    // the note's duration + 30% seems to work well:
    int pauseBetweenNotes = noteDuration * 1.30;
    delay(pauseBetweenNotes);
    // stop the tone playing:
    noTone(m.speakerPin);
  }
}

